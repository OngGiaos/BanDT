package Model;

/**
 *
 * @author TGDD
 */
public class NumberOfItem {

    String email;
    int number;

    public NumberOfItem() {
    }

    public NumberOfItem(String email, int number) {
        this.email = email;
        this.number = number;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

}
